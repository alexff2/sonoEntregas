CREATE VIEW [dbo].[VIEW_DELIVERYS] AS
SELECT A.ID, A.DESCRIPTION, D.DESCRIPTION CAR, D.PLATE, C.DESCRIPTION ASSISTANT, B.DESCRIPTION DRIVER FROM SONOENTREGAS..DELIVERYS A
INNER JOIN USERS B ON A.ID_DRIVER = B.ID
INNER JOIN USERS C ON A.ID_ASSISTANT = C.ID
INNER JOIN CARS D ON A.ID_CAR = D.ID	

GO


